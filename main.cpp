#include "std.h"

// Это учебная программа

int main() {
    int num1, num2;

    cout << "Input first number: ";
    cin >> num1;
    cout << "Input second number: ";
    cin >> num2;

    cout << "Sum = " << num1 + num2 << "\n";
    cout << "Diff = " << num1 - num2 << "\n";
    cout << "Mult = " << num1 * num2 << "\n";
    cout << "Quo = " << num1 / num2 << "\n";

    return 0;
}